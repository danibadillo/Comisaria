    private void botonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarActionPerformed
        policia = new Policia();
        policia.setIdPolicia(Integer.parseInt(textoId.getText()));
        Policia policia2 = new Policia();
        if(textoNumPlaca.getText().isEmpty()){
            policia2.setNumPlaca(null);
        }else{
            policia2.setNumPlaca(textoNumPlaca.getText());
        }
        if(textoEdad.getText().isEmpty()){
            policia2.setEdad(0);
        }else{
            policia2.setEdad(Integer.parseInt(textoEdad.getText()));
        }
        if(dptoSeleccionado){
            policia2.setDepartamento(comboBoxDpto.getSelectedItem().toString());
            dptoSeleccionado = false;
        }else{
            policia2.setDepartamento(null);
        }
        if(fotoSeleccionada){
            policia2.setPathFoto(textoFoto.getText());
            fotoSeleccionada = true;
        }else{
            policia2.setPathFoto(null);
        }
        try {
            if(!textoId.getText().isEmpty()){
                int lineasModificadas = jdbcdao.modificarPolicia(policia, policia2);
                JOptionPane.showMessageDialog(null, "Se ha modificado con exito "+lineasModificadas+" policia", "Mantenimiento de policias", JOptionPane.INFORMATION_MESSAGE);

            }else{
                JOptionPane.showMessageDialog(null, "El Policia no existe en la BD", "Mantenimiento de policias", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al modificar policia\n"+ex.getMessage(), "Mantenimiento de policias", JOptionPane.WARNING_MESSAGE);
//            Logger.getLogger(PoliciasMantenimiento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonModificarActionPerformed

