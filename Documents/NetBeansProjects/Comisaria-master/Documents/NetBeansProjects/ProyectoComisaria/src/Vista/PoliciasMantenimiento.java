    private void botonInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInsertarActionPerformed
        policia = new Policia();
        policia.setNombre(textoNombre.getText());
        policia.setNumPlaca(textoNumPlaca.getText());
        policia.setEdad(Integer.parseInt(textoEdad.getText()));
        policia.setDepartamento(comboBoxDpto.getSelectedItem().toString());
        policia.setPathFoto(textoFoto.getText());
        try {
            if(dptoSeleccionado && fotoSeleccionada){
                jdbcdao.insertarPolicia(policia);
                dptoSeleccionado = false;
                fotoSeleccionada = false;
                JOptionPane.showMessageDialog(null, "Policia insertado", "Mantenimiento de policias", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al insertar policia\n"+ex.getMessage(), "Mantenimiento de policias", JOptionPane.WARNING_MESSAGE);
//            Logger.getLogger(PoliciasMantenimiento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonInsertarActionPerformed

    private void botonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarActionPerformed
        policia = new Policia();
        policia.setIdPolicia(Integer.parseInt(textoId.getText()));
        try {
            if(jdbcdao.comprobarPolicia(policia)){
                int opcion = JOptionPane.showConfirmDialog(null, "¿Esta seguro de querer borrar el policia?\n"+jdbcdao.mostrarDatosPolicia(policia), "Mantenimiento de policias",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if(opcion == 0){
                    int lineasBorradas = jdbcdao.borrarPolicia(policia);
                    JOptionPane.showMessageDialog(null, "Se ha borrado con exito "+lineasBorradas+" policia", "Mantenimiento de policias", JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "El Policia no existe en la BD", "Mantenimiento de policias", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al borrar policia\n"+ex.getMessage(), "Mantenimiento de policias", JOptionPane.WARNING_MESSAGE);
//            Logger.getLogger(PoliciasMantenimiento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonBorrarActionPerformed

    private void textoFotoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_textoFotoMouseClicked
        JFileChooser chooser = new JFileChooser();
        int returnVal = chooser.showOpenDialog(null);
        if(returnVal == JFileChooser.APPROVE_OPTION) {
            textoFoto.setText(chooser.getSelectedFile().getAbsolutePath());
            fotoSeleccionada = true;
        }
    }//GEN-LAST:event_textoFotoMouseClicked

